#Convercion de tipos de datos


# Convertir de cadena a numero
numero_cadena_int = '10'
numero_cadena_float = '10.15'
numero = 10
numero_cadena_int = int(numero_cadena_int)
numero_cadena_float = float(numero_cadena_float)
numero = str(numero)



print(f'cadena a int: {numero_cadena_int}')
print(f'cadena a float: {numero_cadena_float}')
print(f'numero a str: {numero}')
