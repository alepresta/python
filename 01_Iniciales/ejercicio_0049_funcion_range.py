print('************ FUNCION RANGE ************')

print('Secuencia del 0 al 4: ')
for i in range(5):
    print(i, end=' ')
print('\n')

print('Secuencia del 10 al 20: ')
for i in range(10,20+1):
    print(i, end=' ')
print('\n')

print('Secuencia del 20 al 30: ')
for i in range(20,30+1,2):
    print(i, end=' ')








